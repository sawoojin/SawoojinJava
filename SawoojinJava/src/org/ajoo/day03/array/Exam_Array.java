package org.ajoo.day03.array;

public class Exam_Array {

	public static void main(String[] args) {
		int nums []; // 배열 선언
		nums = new int [10];
		nums[0] = 11;
		System.out.println(nums[0]);;
		System.out.println(nums.length);
		for(int i = 0; i < 10; i++) {
			System.out.println(nums[i]);
		}

	}

}
